class TestWaterLESParse:
    def test_parse_and_cell(self, water_les_root):
        """ac-001: WaterLESSource parses extxyz and propagates periodic cell."""
        src = WaterLESSource(water_les_root, split="test")
        assert len(src) == 2
        sample = src[0]
        assert set(sample.keys()) == {"Z", "pos", "cell", "energy", "forces"}
        # 2 H₂O = 6 atoms in fixture-scaled config.
        assert sample["Z"].shape == (6,)
        assert sample["Z"].dtype == torch.long
        assert sample["pos"].shape == (6, 3)
        assert sample["pos"].dtype == torch.float32
        assert sample["forces"].shape == (6, 3)
        assert sample["forces"].dtype == torch.float32
        assert sample["cell"].shape == (3, 3)
        # Cubic 12 Å diagonal preserved (atol below float32 ulp at this scale).
        expected_cell = torch.eye(3, dtype=torch.float32) * 12.0
        assert torch.allclose(sample["cell"], expected_cell, atol=1e-4)
        # Energy is a Python float (not a tensor) per the flat-sample contract.
        assert isinstance(sample["energy"], float)


class TestWaterLESSplit:
    def test_split_deterministic(self, water_les_root):
        """ac-002: WaterLESSource split is deterministic 0.95/0.05 from `train-…xyz`.

        The fixture writes 40 train frames, so we expect 38 train / 2 val
        samples, with the val slice being the *tail* of the input file
        (deterministic, no shuffle), and `test-…xyz` mapping to a third
        disjoint split.
        """
        train = WaterLESSource(water_les_root, split="train")
        val = WaterLESSource(water_les_root, split="val")
        test = WaterLESSource(water_les_root, split="test")
        assert len(train) == 38
        assert len(val) == 2
        assert len(test) == 2
        # Train and val together must reconstruct the full train file by
        # deterministic slice — no overlap, no missing frame, no shuffle.
        full_train_energies = sorted(
            [float(train[i]["energy"]) for i in range(len(train))]
            + [float(val[i]["energy"]) for i in range(len(val))]
        )
        # Generated by conftest: 40 frames with energy = -60.0 - 0.1 * i.
        expected = sorted([-60.0 - 0.1 * i for i in range(40)])
        for got, want in zip(full_train_energies, expected):
            assert got == pytest.approx(want, abs=1e-4)
        # Val slice = tail of the train file (deterministic — last 2 frames).
        val_energies = [float(val[i]["energy"]) for i in range(len(val))]
        assert val_energies == [
            pytest.approx(-60.0 - 0.1 * 38, abs=1e-4),
            pytest.approx(-60.0 - 0.1 * 39, abs=1e-4),
        ]
        # Train slice never overlaps val.
        train_energies = {float(train[i]["energy"]) for i in range(len(train))}
        for ve in val_energies:
            assert ve not in train_energies

    def test_split_basenames_match_upstream_yaml(self, water_les_root):
        """ac-002 (provenance): basenames match les_fit YAML constants."""
        # The class records the upstream-pinned filenames as a class attr;
        # this guards against silent renames during refactor.
        assert WaterLESSource.TRAIN_FILE == "train-H2O_RPBE-D3.xyz"
        assert WaterLESSource.TEST_FILE == "test-H2O_RPBE-D3.xyz"
        # And the deterministic ratio matches Allegro-LES water YAML.
        assert WaterLESSource.TRAIN_VAL_RATIO == (0.95, 0.05)


class TestWaterLESChecksum:
    def test_checksum_mismatch(self, water_les_root, water_les_checksums, monkeypatch):
        """ac-003: corrupted byte triggers ValueError with both digests in message."""
        # Pin the source's expected digest dict to the fixture's real values
        # (so verification is wired correctly), then corrupt one file.
        monkeypatch.setattr(WaterLESSource, "_CHECKSUMS", dict(water_les_checksums))
        train_path = water_les_root / "train-H2O_RPBE-D3.xyz"
        original = train_path.read_bytes()
        # Flip a non-newline byte in the middle to keep it parseable but mismatched.
        idx = len(original) // 2
        flip = bytes([(original[idx] ^ 0x01)])
        train_path.write_bytes(original[:idx] + flip + original[idx + 1 :])

        with pytest.raises(ValueError, match="checksum mismatch"):
            WaterLESSource(water_les_root, split="train", verify_checksum=True)

    def test_checksum_message_contains_both_digests(
        self, water_les_root, water_les_checksums, monkeypatch
    ):
        """ac-003: error message contains expected and actual SHA-256."""
        monkeypatch.setattr(WaterLESSource, "_CHECKSUMS", dict(water_les_checksums))
        train_path = water_les_root / "train-H2O_RPBE-D3.xyz"
        train_path.write_bytes(train_path.read_bytes() + b"\n")

        with pytest.raises(ValueError) as exc:
            WaterLESSource(water_les_root, split="train", verify_checksum=True)
        msg = str(exc.value)
        # Expected digest is what the fixture computed; actual is what we
        # produced after appending. Both must appear verbatim.
        assert water_les_checksums["train-H2O_RPBE-D3.xyz"] in msg
        # Actual is hex-only — assert hex-shaped second sha256 exists.
        import re

        hexes = re.findall(r"\b[0-9a-f]{64}\b", msg)
        assert len(hexes) == 2 and hexes[0] != hexes[1]

    def test_checksum_skipped_by_default(self, water_les_root, monkeypatch):
        """ac-003 (corollary): default `verify_checksum=False` does NOT raise."""
        monkeypatch.setattr(WaterLESSource, "_CHECKSUMS", {"train-H2O_RPBE-D3.xyz": "deadbeef" * 8})
        # Should construct without error, since verify_checksum default is False.
        WaterLESSource(water_les_root, split="train")


class TestWaterLESSourceID:
    def test_source_id_stable(self, water_les_root):
        """ac-006: source_id is deterministic and contains required tags."""
        src = WaterLESSource(water_les_root, split="train")
        sid_a = src.source_id
        sid_b = src.source_id
        assert sid_a == sid_b
        assert "water_les" in sid_a
        assert "split=train" in sid_a
        # Size + sample count must appear too.
        train_size = (water_les_root / "train-H2O_RPBE-D3.xyz").stat().st_size
        assert f"size={train_size}" in sid_a
        assert f"n={len(src)}" in sid_a

    def test_source_id_split_separation(self, water_les_root):
        """ac-006: distinct splits yield distinct source_ids."""
        train = WaterLESSource(water_les_root, split="train").source_id
        val = WaterLESSource(water_les_root, split="val").source_id
        test = WaterLESSource(water_les_root, split="test").source_id
        assert len({train, val, test}) == 3


class TestWaterLESTargetSchema:
    def test_target_schema(self):
        """ac-007: TARGET_SCHEMA exposes graph={energy} + atom={forces}."""
        assert isinstance(WaterLESSource.TARGET_SCHEMA, TargetSchema)
        assert WaterLESSource.TARGET_SCHEMA.graph_level == frozenset({"energy"})
        assert WaterLESSource.TARGET_SCHEMA.atom_level == frozenset({"forces"})


class TestWaterLESErrors:
    def test_unknown_split_rejected(self, water_les_root):
        with pytest.raises(ValueError, match="split"):
            WaterLESSource(water_les_root, split="not_a_split")  # type: ignore[arg-type]

    def test_offline_without_files_raises(self, tmp_path):
        empty = tmp_path / "empty"
        empty.mkdir()
        with pytest.raises(FileNotFoundError):
            WaterLESSource(empty, split="train")
