def _random_neutral_charges(rng: np.random.Generator, n: int) -> torch.Tensor:
    q = rng.standard_normal(n)
    q -= q.mean()
    return torch.from_numpy(q).to(torch.float64)


def test_translation_invariance_realspace() -> None:
    rng = np.random.default_rng(1)
    pos = torch.from_numpy(rng.uniform(-3.0, 3.0, size=(6, 3))).to(torch.float64)
    q = _random_neutral_charges(rng, 6)
    pot = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR)
    u0 = pot.forward(q=q, pos=pos, cell=None)["pot"].item()
    t = torch.tensor([2.5, -1.7, 3.1], dtype=torch.float64)
    u1 = pot.forward(q=q, pos=pos + t, cell=None)["pot"].item()
    assert abs(u0 - u1) < 1e-7, f"translation: U0={u0}, U1={u1}, diff={abs(u0 - u1)}"


def test_translation_invariance_reciprocal() -> None:
    rng = np.random.default_rng(2)
    L = 12.0
    pos = torch.from_numpy(rng.uniform(0.0, L, size=(8, 3))).to(torch.float64)
    q = _random_neutral_charges(rng, 8)
    cell = L * torch.eye(3, dtype=torch.float64)
    pot = EwaldMultipoleEnergy(sigma=1.0, dl=2.0, prefactor=PREFACTOR)
    u0 = pot.forward(q=q, pos=pos, cell=cell)["pot"].item()
    t = torch.tensor([2.5, -1.7, 3.1], dtype=torch.float64)
    u1 = pot.forward(q=q, pos=pos + t, cell=cell)["pot"].item()
    assert abs(u0 - u1) < 1e-7, f"translation: U0={u0}, U1={u1}, diff={abs(u0 - u1)}"


def _random_rotation_matrix(seed: int) -> torch.Tensor:
    """Random SO(3) matrix via QR decomposition of a random Gaussian matrix."""
    rng = np.random.default_rng(seed)
    A = rng.standard_normal((3, 3))
    Q, R = np.linalg.qr(A)
    Q = Q @ np.diag(np.sign(np.diag(R)))
    if np.linalg.det(Q) < 0:
        Q[:, 0] = -Q[:, 0]
    return torch.from_numpy(Q).to(torch.float64)


def test_rotation_invariance_realspace() -> None:
    rng = np.random.default_rng(3)
    pos = torch.from_numpy(rng.uniform(-3.0, 3.0, size=(6, 3))).to(torch.float64)
    q = _random_neutral_charges(rng, 6)
    mu = torch.from_numpy(rng.standard_normal((6, 3))).to(torch.float64)
    pot = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR)

    u0 = pot.forward(q=q, pos=pos, mu=mu, cell=None)["pot"].item()
    R = _random_rotation_matrix(seed=10)
    pos_rot = pos @ R.T
    mu_rot = mu @ R.T
    u1 = pot.forward(q=q, pos=pos_rot, mu=mu_rot, cell=None)["pot"].item()
    assert abs(u0 - u1) < 1e-7, f"rotation: U0={u0}, U1={u1}"


def test_permutation_equivariance() -> None:
    rng = np.random.default_rng(4)
    n = 5
    pos = torch.from_numpy(rng.uniform(-3.0, 3.0, size=(n, 3))).to(torch.float64)
    q = _random_neutral_charges(rng, n)
    pot = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR)

    perm = torch.randperm(n, generator=torch.Generator().manual_seed(7))
    u0 = pot.forward(q=q, pos=pos, cell=None)["pot"].item()
    u1 = pot.forward(q=q[perm], pos=pos[perm], cell=None)["pot"].item()
    assert abs(u0 - u1) < 1e-9, f"permutation: U0={u0}, U1={u1}, diff={abs(u0 - u1)}"


