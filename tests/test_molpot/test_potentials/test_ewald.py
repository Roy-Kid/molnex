@pytest.mark.parametrize("sigma", [0.5, 1.0, 2.0])
def test_sigma_independence_at_long_range(sigma: float) -> None:
    """Two charges at d ≫ σ must give same U across σ to 1e-5."""
    pos = torch.tensor([[0.0, 0.0, 0.0], [15.0, 0.0, 0.0]], dtype=torch.float64)
    q = torch.tensor([1.0, -1.0], dtype=torch.float64)
    pot = EwaldMultipoleEnergy(sigma=sigma, prefactor=PREFACTOR)
    out = pot.forward(q=q, pos=pos, cell=None)
    expected = -NORM_CONST / 15.0  # bare Coulomb at long range
    assert abs(float(out["pot"]) - expected) < 1e-5, (
        f"σ={sigma}: U={float(out['pot'])} vs {expected}"
    )


