def test_single_atom_self_interaction_toggle() -> None:
    """Single isolated atom: RSI=True ⇒ U=0; RSI=False ⇒ analytic self-energy."""
    pos = torch.tensor([[0.0, 0.0, 0.0]], dtype=torch.float64)
    q = torch.tensor([1.0], dtype=torch.float64)

    pot_true = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR, remove_self_interaction=True)
    u_true = float(pot_true.forward(q=q, pos=pos, cell=None)["pot"])
    assert abs(u_true) < 1e-12, f"RSI=True should give zero, got {u_true}"

    pot_false = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR, remove_self_interaction=False)
    u_false = float(pot_false.forward(q=q, pos=pos, cell=None)["pot"])
    expected = PREFACTOR / ((2.0 * math.pi) ** 1.5)  # 1²/(σ·(2π)^{3/2}) · prefactor with σ=1
    assert abs(u_false - expected) < 1e-12, f"RSI=False: {u_false} vs analytic {expected}"


# ---------------------------------------------------------------------------
# Full SO(3) equivariance — energy invariance + field/quantity equivariance
#
# Under a rotation R applied jointly to {pos, μ, Q, cell}:
#   pos    → R pos      (vector)
#   μ      → R μ        (vector)
#   Q      → R Q Rᵀ     (rank-2 symmetric tensor)
#   cell   → cell Rᵀ    (lattice vectors transform like positions)
#   U      = U          (scalar — invariant)
#   φ(rᵢ)  = φ_orig(rᵢ) (scalar field at atom — invariant)
#   E(rᵢ)  → R E_orig(rᵢ)  (vector field at atom — equivariant)
#
# Tolerance is 1e-9 for invariants (algebraic identity in float64) and
# 1e-9 for equivariance after re-rotating the original field back.
# ---------------------------------------------------------------------------


def _rotate_quadrupole(Q: torch.Tensor, R: torch.Tensor) -> torch.Tensor:
    """``Q[n] → R · Q[n] · Rᵀ`` for a rank-2 tensor stored as ``(N, 3, 3)``."""
    return torch.einsum("ab,nbc,dc->nad", R, Q, R)


def _random_traceless_symmetric_Q(rng: np.random.Generator, n: int) -> torch.Tensor:
    raw = rng.standard_normal((n, 3, 3))
    sym = 0.5 * (raw + raw.transpose(0, 2, 1))
    trace_per_atom = np.einsum("nii->n", sym)
    Q_traceless = sym - (trace_per_atom / 3.0)[:, None, None] * np.eye(3)[None, :, :]
    return torch.from_numpy(Q_traceless).to(torch.float64)


class TestSO3Equivariance:
    """Joint rotation of {pos, μ, Q, cell} leaves U invariant and rotates E."""

    def test_realspace_full_multipole(self) -> None:
        rng = np.random.default_rng(7)
        n = 6
        pos = torch.from_numpy(rng.uniform(-3.0, 3.0, size=(n, 3))).to(torch.float64)
        q = _random_neutral_charges(rng, n)
        mu = torch.from_numpy(rng.standard_normal((n, 3))).to(torch.float64)
        Q = _random_traceless_symmetric_Q(rng, n)
        pot = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR)

        ref = pot.forward(q=q, pos=pos, mu=mu, Q=Q, cell=None)
        R = _random_rotation_matrix(seed=11)
        rot = pot.forward(
            q=q,
            pos=pos @ R.T,
            mu=mu @ R.T,
            Q=_rotate_quadrupole(Q, R),
            cell=None,
        )

        # Energy invariant
        assert abs(float(ref["pot"]) - float(rot["pot"])) < 1e-9, (
            f"realspace U not invariant under SO(3): "
            f"ref={float(ref['pot'])}, rot={float(rot['pot'])}"
        )
        # phi invariant at each atom (atom-by-atom — atoms don't reorder)
        torch.testing.assert_close(ref["phi"], rot["phi"], atol=1e-9, rtol=1e-9)
        # field equivariant: rot.field[i] = R · ref.field[i]
        torch.testing.assert_close(ref["field"] @ R.T, rot["field"], atol=1e-9, rtol=1e-9)

    def test_reciprocal_full_multipole(self) -> None:
        rng = np.random.default_rng(8)
        L = 10.0
        n = 8
        pos = torch.from_numpy(rng.uniform(0.0, L, size=(n, 3))).to(torch.float64)
        q = _random_neutral_charges(rng, n)
        mu = torch.from_numpy(rng.standard_normal((n, 3))).to(torch.float64)
        Q = _random_traceless_symmetric_Q(rng, n)
        cell = L * torch.eye(3, dtype=torch.float64)
        pot = EwaldMultipoleEnergy(sigma=1.0, dl=2.0, prefactor=PREFACTOR)

        ref = pot.forward(q=q, pos=pos, mu=mu, Q=Q, cell=cell)
        R = _random_rotation_matrix(seed=12)
        rot = pot.forward(
            q=q,
            pos=pos @ R.T,
            mu=mu @ R.T,
            Q=_rotate_quadrupole(Q, R),
            cell=cell @ R.T,  # lattice vectors rotate with positions
        )

        # k-grid resolution is dl-quantized so reciprocal SO(3) is a soft
        # 1e-6, not the algebraic 1e-9 of realspace; mode count differs
        # across orientations.
        assert abs(float(ref["pot"]) - float(rot["pot"])) < 1e-6, (
            f"reciprocal U not invariant under SO(3): "
            f"ref={float(ref['pot'])}, rot={float(rot['pot'])}"
        )
        torch.testing.assert_close(ref["phi"], rot["phi"], atol=1e-6, rtol=1e-6)
        torch.testing.assert_close(ref["field"] @ R.T, rot["field"], atol=1e-6, rtol=1e-6)

    def test_induced_response_equivariance(self) -> None:
        """``q_induced`` invariant; ``u_induced`` equivariant. Realspace path."""
        rng = np.random.default_rng(9)
        n = 5
        pos = torch.from_numpy(rng.uniform(-3.0, 3.0, size=(n, 3))).to(torch.float64)
        q = _random_neutral_charges(rng, n)
        kappa = torch.from_numpy(np.abs(rng.standard_normal(n)) + 0.1).to(torch.float64)
        alpha = torch.from_numpy(np.abs(rng.standard_normal(n)) + 0.1).to(torch.float64)
        pot = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR)

        ref = pot.forward(q=q, pos=pos, kappa=kappa, alpha=alpha, cell=None)
        R = _random_rotation_matrix(seed=13)
        rot = pot.forward(q=q, pos=pos @ R.T, kappa=kappa, alpha=alpha, cell=None)

        # q_induced = -κ · φ; both κ and φ are scalars, so invariant.
        torch.testing.assert_close(ref["q_induced"], rot["q_induced"], atol=1e-9, rtol=1e-9)
        # u_induced = α · E; α scalar, E rotates, so u_induced rotates too.
        torch.testing.assert_close(ref["u_induced"] @ R.T, rot["u_induced"], atol=1e-9, rtol=1e-9)


# ---------------------------------------------------------------------------
# torch.compile round-trip
#
# `EwaldMultipoleEnergy.forward` uses Python control flow (`for raw in
# unique`, `if cell is None`) and `.item()` calls (Nk[i], unique batch
# index), all of which force graph breaks under `torch.compile`. With
# `fullgraph=False` the compile must still execute and produce
# numerically-equivalent output — that's the contract this test pins.
# ---------------------------------------------------------------------------


class TestTorchCompile:
    @pytest.fixture(scope="class")
    def realspace_inputs(self):
        rng = np.random.default_rng(101)
        pos = torch.from_numpy(rng.uniform(-3.0, 3.0, size=(6, 3))).to(torch.float64)
        q = _random_neutral_charges(rng, 6)
        mu = torch.from_numpy(rng.standard_normal((6, 3))).to(torch.float64)
        return q, pos, mu

    def test_compile_realspace(self, realspace_inputs) -> None:
        """Compiled forward must match eager on the realspace path."""
        q, pos, mu = realspace_inputs
        pot = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR)

        eager = pot.forward(q=q, pos=pos, mu=mu, cell=None)

        compiled = torch.compile(pot.forward, fullgraph=False, dynamic=False)
        compiled_out = compiled(q=q, pos=pos, mu=mu, cell=None)

        torch.testing.assert_close(eager["pot"], compiled_out["pot"], atol=1e-10, rtol=1e-10)
        torch.testing.assert_close(eager["phi"], compiled_out["phi"], atol=1e-10, rtol=1e-10)
        torch.testing.assert_close(eager["field"], compiled_out["field"], atol=1e-10, rtol=1e-10)

    def test_compile_reciprocal(self) -> None:
        """Compiled forward must match eager on the reciprocal path."""
        rng = np.random.default_rng(102)
        L = 10.0
        n = 6
        pos = torch.from_numpy(rng.uniform(0.0, L, size=(n, 3))).to(torch.float64)
        q = _random_neutral_charges(rng, n)
        cell = L * torch.eye(3, dtype=torch.float64)
        pot = EwaldMultipoleEnergy(sigma=1.0, dl=2.0, prefactor=PREFACTOR)

        eager = pot.forward(q=q, pos=pos, cell=cell)

        compiled = torch.compile(pot.forward, fullgraph=False, dynamic=False)
        compiled_out = compiled(q=q, pos=pos, cell=cell)

        torch.testing.assert_close(eager["pot"], compiled_out["pot"], atol=1e-10, rtol=1e-10)
        torch.testing.assert_close(eager["phi"], compiled_out["phi"], atol=1e-10, rtol=1e-10)
        torch.testing.assert_close(eager["field"], compiled_out["field"], atol=1e-10, rtol=1e-10)

    def test_compile_with_induced_response(self, realspace_inputs) -> None:
        """Compiled forward must preserve the κ/α inline response."""
        q, pos, _mu = realspace_inputs
        rng = np.random.default_rng(103)
        kappa = torch.from_numpy(np.abs(rng.standard_normal(6)) + 0.1).to(torch.float64)
        alpha = torch.from_numpy(np.abs(rng.standard_normal(6)) + 0.1).to(torch.float64)
        pot = EwaldMultipoleEnergy(sigma=1.0, prefactor=PREFACTOR)

        eager = pot.forward(q=q, pos=pos, kappa=kappa, alpha=alpha, cell=None)
        compiled = torch.compile(pot.forward, fullgraph=False, dynamic=False)
        compiled_out = compiled(q=q, pos=pos, kappa=kappa, alpha=alpha, cell=None)

        torch.testing.assert_close(eager["pot"], compiled_out["pot"], atol=1e-10, rtol=1e-10)
        torch.testing.assert_close(
            eager["q_induced"],
            compiled_out["q_induced"],
            atol=1e-10,
            rtol=1e-10,
        )
        torch.testing.assert_close(
            eager["u_induced"],
            compiled_out["u_induced"],
            atol=1e-10,
            rtol=1e-10,
        )
