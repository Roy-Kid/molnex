# Isolated H₂O dipole field
# ---------------------------------------------------------------------------


def _build_water_with_probe(probe_r: float = 30.0) -> tuple[torch.Tensor, torch.Tensor]:
    """TIP3P water with a fictional zero-charge ghost atom at probe distance.

    The probe atom carries q=0, so the per-atom field at its position is
    exactly the field generated by the three real water atoms.
    """
    # TIP3P geometry (Å): O at origin, two H's at the 104.52° angle.
    half_angle = math.radians(104.52 / 2.0)
    OH = 0.9572  # Å
    pos = torch.tensor(
        [
            [0.0, 0.0, 0.0],  # O
            [OH * math.sin(half_angle), 0.0, OH * math.cos(half_angle)],  # H1
            [-OH * math.sin(half_angle), 0.0, OH * math.cos(half_angle)],  # H2
            [0.0, 0.0, probe_r],  # ghost probe
        ],
        dtype=torch.float64,
    )
    # TIP3P partial charges
    q = torch.tensor([-0.834, 0.417, 0.417, 0.0], dtype=torch.float64)
    return pos, q


def test_isolated_water_dipole_field_at_30_angstroms() -> None:
    """Field at ghost probe 30 Å away matches dipole asymptote within 1 %."""
    probe_r = 30.0
    pos, q = _build_water_with_probe(probe_r=probe_r)

    pot = EwaldMultipoleEnergy(sigma=0.3, prefactor=PREFACTOR)
    out = pot.forward(q=q, pos=pos, cell=None)

    field_at_probe = out["field"][3]  # (3,) — field at the ghost atom
    field_z = float(field_at_probe[2])

    # Compute the molecular dipole moment from charges (origin-independent
    # only for neutral systems — TIP3P is exactly neutral).
    mu_mol = (q.unsqueeze(-1) * pos).sum(dim=0)  # (3,)
    mu_norm = float(mu_mol.norm())  # in e·Å
    # Axial-dipole field magnitude: 2|μ|/(4πε₀·r³).
    expected_field_mag = 2.0 * mu_norm * NORM_CONST / probe_r**3

    # The probe sits along +z; the dipole points along +z (negative oxygen
    # at origin, positive H's at +z). Field along the dipole axis at +z
    # has the same sign as the dipole.
    rel_err = abs(field_z - expected_field_mag) / expected_field_mag
    assert rel_err < 1e-2, (
        f"H₂O probe field {field_z:.4e} V/Å vs dipole asymptote "
        f"{expected_field_mag:.4e}, rel_err {rel_err:.2e}"
    )


